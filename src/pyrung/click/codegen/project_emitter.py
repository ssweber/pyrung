def _generate_tags_file(collection: _OperandCollection) -> str:
    """Generate tags.py: declarations + TagMap."""
    lines: list[str] = [_GENERATED_HEADER, ""]

    # Imports
    _emit_tags_imports(lines, collection)
    lines.append("")

    # Tag declarations
    has_flat_tags = any(
        op not in collection.semantic_operands and op not in collection.timer_counter_operands
        for op in collection.tags
    )
    if has_flat_tags:
        lines.append("# --- Tags ---")
        _emit_tag_declarations(lines, collection, suppress_comments=True)
        lines.append("")

    if collection.plain_blocks:
        lines.append("# --- Blocks ---")
        _emit_plain_block_declarations(lines, collection)
        lines.append("")

    # Structure declarations
    if collection.structures:
        lines.append("# --- Structures ---")
        _emit_structure_declarations(lines, collection)
        lines.append("")

    # TagMap
    lines.append("# --- Tag Map ---")
    _emit_tag_map(lines, collection)
    lines.append("")

    return "\n".join(lines) + "\n"


def _emit_tags_imports(lines: list[str], collection: _OperandCollection) -> None:
    """Emit imports for tags.py (types, blocks, TagMap)."""
    core: list[str] = []

    # Block/TagType for reconstructed plain named blocks
    if collection.plain_blocks:
        core.append("Block")
        core.append("TagType")

    # Structure decorators
    has_named_array = any(s.structure_type == "named_array" for s in collection.structures)
    has_udt = any(s.structure_type == "udt" for s in collection.structures)
    has_retentive = any(any(v for v in s.field_retentive.values()) for s in collection.structures)
    if has_named_array:
        core.append("named_array")
    if has_udt:
        core.append("udt")
    if has_retentive:
        core.append("Field")

    # Built-in Timer/Counter UDTs
    if collection.used_instructions & {"on_delay", "off_delay"}:
        core.append("Timer")
    if collection.used_instructions & {"count_up", "count_down"}:
        core.append("Counter")

    # Tag types
    for tt in sorted(collection.used_types):
        if tt not in core:
            core.append(tt)

    if core:
        lines.append(f"from pyrung import {', '.join(core)}")

    # Click imports
    click: list[str] = ["TagMap"]
    for bv in sorted(collection.used_blocks):
        click.append(bv)
    lines.append(f"from pyrung.click import {', '.join(click)}")

    # Expression functions
    if collection.used_expr_funcs:
        expr = sorted(collection.used_expr_funcs)
        lines.append(f"from pyrung.core.expression import {', '.join(expr)}")


