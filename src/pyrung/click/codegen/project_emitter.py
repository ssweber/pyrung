# main.py
# ---------------------------------------------------------------------------


def _generate_main_file(
    rungs: list[_AnalyzedRung],
    collection: _OperandCollection,
    nicknames: dict[str, str] | None,
    subroutines: list[_SubroutineInfo],
    *,
    structured_map: TagMap | None = None,
    call_func_map: dict[str, str] | None = None,
) -> str:
    """Generate main.py: Program with main rungs + call() statements."""
    refs = _scan_file_refs(rungs, collection, nicknames, call_func_map=call_func_map)

    lines: list[str] = [_GENERATED_HEADER, ""]

    # pyrung imports
    has_calls = bool(subroutines)
    _emit_logic_imports(lines, refs, is_main=True, has_calls=has_calls)

    # from tags import mapping, ...
    refs_with_mapping = _FileRefs(
        tag_var_names=set(refs.tag_var_names),
        block_var_names=set(refs.block_var_names),
        range_var_names=set(refs.range_var_names),
        structure_names=set(refs.structure_names),
    )
    _emit_tags_import_line(lines, refs_with_mapping, include_mapping=True)

    # from subroutines.X import X
    if subroutines:
        for sub in subroutines:
            func_name = call_func_map[sub.name] if call_func_map else _slugify(sub.name)
            lines.append(f"from subroutines.{func_name} import {func_name}")

    lines.append("")

    # Program body (main rungs only, no subroutine definitions)
    lines.append("with Program(strict=False) as logic:")
    if rungs:
        _emit_rung_sequence(
            lines,
            rungs,
            collection,
            nicknames,
            indent=1,
            structured_map=structured_map,
            call_func_map=call_func_map,
        )
    else:
        lines.append("    pass")
    lines.append("")

    return "\n".join(lines) + "\n"


# ---------------------------------------------------------------------------
# Shared import helpers
# ---------------------------------------------------------------------------


def _emit_logic_imports(
    lines: list[str],
    refs: _FileRefs,
    *,
    is_main: bool = False,
    is_subroutine: bool = False,
    has_calls: bool = False,
) -> None:
    """Emit ``from pyrung import ...`` for a logic file (main or subroutine)."""
    parts: list[str] = []

    if is_main:
        parts.append("Program")
    parts.append("Rung")

    if has_calls or refs.subroutine_func_names:
        parts.append("call")
    if is_subroutine:
        parts.append("subroutine")

    # Built-in Timer/Counter UDTs
    if refs.used_instructions & {"on_delay", "off_delay"}:
        parts.append("Timer")
    if refs.used_instructions & {"count_up", "count_down"}:
        parts.append("Counter")

    # Conditions
    if refs.has_Or:
        parts.append("Or")
    if refs.has_And:
        parts.append("And")
    for cw in sorted(refs.used_conditions):
        parts.append(cw)

    # Instructions
    _INSTR_MAP = {
        "out": "out",
        "latch": "latch",
        "reset": "reset",
        "copy": "copy",
        "blockcopy": "blockcopy",
        "fill": "fill",
        "math": "calc",
        "on_delay": "on_delay",
        "off_delay": "off_delay",
        "count_up": "count_up",
        "count_down": "count_down",
        "shift": "shift",
        "search": "search",
        "pack_bits": "pack_bits",
        "pack_words": "pack_words",
        "pack_text": "pack_text",
        "unpack_to_bits": "unpack_to_bits",
        "unpack_to_words": "unpack_to_words",
        "event_drum": "event_drum",
        "time_drum": "time_drum",
        "return": "return_early",
    }
    for instr_name in sorted(refs.used_instructions):
        import_name = _INSTR_MAP.get(instr_name)
        if import_name and import_name not in parts:
            parts.append(import_name)

    if refs.has_branch:
        parts.append("branch")
    if refs.has_comment:
        parts.append("comment")
    if refs.has_forloop:
        parts.append("forloop")

    # Copy converters
    for cc in sorted(refs.used_copy_converters):
        parts.append(cc)

    lines.append(f"from pyrung import {', '.join(parts)}")

    # Click imports (only if needed)
    click_parts: list[str] = []
    for bv in sorted(refs.used_click_blocks):
        click_parts.append(bv)
    if refs.has_modbus_target:
        click_parts.append("ModbusTcpTarget")
    if refs.has_modbus_rtu_target:
        click_parts.append("ModbusRtuTarget")
    if refs.has_modbus_address:
        click_parts.append("ModbusAddress")
    if "send" in refs.used_instructions:
        click_parts.append("send")
    if "receive" in refs.used_instructions:
        click_parts.append("receive")
    if click_parts:
        lines.append(f"from pyrung.click import {', '.join(click_parts)}")

    # Expression functions
    if refs.used_expr_funcs:
        expr = sorted(refs.used_expr_funcs)
        lines.append(f"from pyrung.core.expression import {', '.join(expr)}")

    if refs.has_system_import:
        lines.append("from pyrung.core.system_points import system")


def _emit_tags_import_line(
    lines: list[str],
    refs: _FileRefs,
    *,
    include_mapping: bool = False,
) -> None:
    """Emit ``from tags import ...`` for the symbols this file references."""
    names: list[str] = []
    if include_mapping:
        names.append("mapping")
    names.extend(sorted(refs.tag_var_names))
    names.extend(sorted(refs.block_var_names))
    names.extend(sorted(refs.range_var_names))
    names.extend(sorted(refs.structure_names))

    if names:
        lines.append(f"from tags import {', '.join(names)}")
