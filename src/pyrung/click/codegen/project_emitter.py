"""Multi-file project output for ladder_to_pyrung_project().

Generates a Python project layout::

    main.py
    tags.py
    subroutines/
        __init__.py
        startup.py
        alarm_handler.py
"""

from __future__ import annotations

from typing import TYPE_CHECKING

from pyrung.click.codegen.collector import _scan_file_refs
from pyrung.click.codegen.emitter import (
    _emit_plain_block_declarations,
    _emit_rung_sequence,
    _emit_structure_declarations,
    _emit_tag_declarations,
    _emit_tag_map,
    _is_trailing_return,
    _prescan_expr_funcs,
)
from pyrung.click.codegen.models import (
    _AnalyzedRung,
    _FileRefs,
    _OperandCollection,
    _SubroutineInfo,
)
from pyrung.click.codegen.utils import _build_sub_name_map, _slugify

if TYPE_CHECKING:
    from pyrung.click.tag_map import TagMap


_GENERATED_HEADER = "# Generated by pyrung from Click ladder CSV"


# ---------------------------------------------------------------------------
# Public entry point
# ---------------------------------------------------------------------------


def _generate_project(
    rungs: list[_AnalyzedRung],
    collection: _OperandCollection,
    nicknames: dict[str, str] | None,
    subroutines: list[_SubroutineInfo],
    *,
    structured_map: TagMap | None = None,
) -> dict[str, str]:
    """Generate a multi-file pyrung project.

    Returns ``{relative_path: content}`` — ready to write to disk or inspect.
    """
    # Pre-scan expression functions so imports are complete.
    _prescan_expr_funcs(rungs, collection, subroutines or None)

    sub_name_map = _build_sub_name_map(subroutines)
    # call_func_map: subroutine display name → Python function identifier
    call_func_map = sub_name_map

    files: dict[str, str] = {}

    # tags.py
    files["tags.py"] = _generate_tags_file(collection)

    # subroutines/
    if subroutines:
        files["subroutines/__init__.py"] = ""
        for sub in subroutines:
            func_name = sub_name_map[sub.name]
            files[f"subroutines/{func_name}.py"] = _generate_subroutine_file(
                sub,
                collection,
                nicknames,
                structured_map=structured_map,
                call_func_map=call_func_map,
            )

    # main.py
    files["main.py"] = _generate_main_file(
        rungs,
        collection,
        nicknames,
        subroutines,
        structured_map=structured_map,
        call_func_map=call_func_map,
    )

    # run.py
    files["run.py"] = _generate_run_file()

    # project_to_csv.py
    files["project_to_csv.py"] = _generate_export_file()

    # .vscode/launch.json
    files[".vscode/launch.json"] = _generate_launch_json()

    # .vscode/extensions.json
    files[".vscode/extensions.json"] = _generate_extensions_json()

    # pyproject.toml
    files["pyproject.toml"] = _generate_pyproject()

    # README
    files["README.md"] = _generate_readme()

    return files


# ---------------------------------------------------------------------------
# run.py
# ---------------------------------------------------------------------------


def _generate_pyproject() -> str:
    """Generate pyproject.toml with pyrung as a dependency."""
    return """\
[project]
name = "plc-logic"
version = "0.1.0"
requires-python = ">=3.11"
dependencies = [
    "pyrung>=0.2.0",
]
"""


def _generate_readme() -> str:
    """Generate README.md with setup instructions."""
    return """\
# Setup

1. Install [uv](https://docs.astral.sh/uv/getting-started/installation/) if you haven't already:

       powershell -ExecutionPolicy ByPass -c "irm https://astral.sh/uv/install.ps1 | iex"

2. Create the virtual environment and install dependencies:

       uv sync

3. Install the pyrung debugger extension for VS Code.
   Download `pyrung-debug-*.vsix` from the
   [latest release](https://github.com/ssweber/pyrung/releases), then:

       code --install-extension pyrung-debug-0.1.0.vsix

4. Open this folder in VS Code, press F5 to debug.

# Loading into Click Programming Software

1. Re-export the project to Click ladder CSV (picks up any edits you've made):

       uv run python project_to_csv.py

2. Install [ClickNick](https://github.com/ssweber/clicknick):

       uv tool install clicknick

3. In Click Programming Software, go to **Ladder → Open in Guided Paste...**
   and point it at the `click_csv` folder.

   ClickNick walks you through pasting each rung and subroutine into Click
   with addresses and nicknames already wired up.
"""


def _generate_extensions_json() -> str:
    """Generate .vscode/extensions.json recommending the pyrung debugger."""
    import json

    config = {
        "recommendations": [
            "ssweber.pyrung-debug",
        ]
    }
    return json.dumps(config, indent=2) + "\n"


def _generate_launch_json() -> str:
    """Generate .vscode/launch.json for the pyrung debugger extension."""
    import json

    config = {
        "version": "0.2.0",
        "configurations": [
            {
                "name": "Debug PLC Logic",
                "type": "pyrung",
                "request": "launch",
                "program": "${workspaceFolder}/run.py",
                "pythonPath": "${workspaceFolder}/.venv/Scripts/python.exe",
            }
        ],
    }
    return json.dumps(config, indent=2) + "\n"


def _generate_run_file() -> str:
    """Generate run.py: instantiate PLC and step the logic."""
    return """\
from pyrung import PLC
from main import logic

runner = PLC(logic)
runner.step()
print(f"Scan {runner.current_state.scan_id}: OK")
"""


def _generate_export_file() -> str:
    """Generate project_to_csv.py: convert the pyrung project back to Click CSV."""
    return '''\
"""Export this pyrung project back to Click ladder CSV files."""

from pathlib import Path

from pyrung.click import pyrung_to_ladder
from main import logic
from tags import mapping

here = Path(__file__).parent
output_dir = here / "csv_output"
bundle = pyrung_to_ladder(logic, mapping)
bundle.write(output_dir)

mapping.to_nickname_file(output_dir / "nicknames.csv")

print(f"Wrote CSV to {output_dir}")
'''


