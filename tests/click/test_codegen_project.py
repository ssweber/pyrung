def _project_from_program(
    program: Program,
    tag_map: TagMap,
    tmp_path: Path,
    *,
    nicknames: dict[str, str] | None = None,
) -> dict[str, str]:
    """Program -> CSV -> ladder_to_pyrung_project -> files dict."""
    bundle = pyrung_to_ladder(program, tag_map)
    csv_dir = tmp_path / "csv"
    bundle.write(csv_dir)
    return ladder_to_pyrung_project(csv_dir, nicknames=nicknames)


def _exec_project(files: dict[str, str], tmp_path: Path) -> dict:
    """Write project files to disk and import them, returning main module namespace."""
    project_dir = tmp_path / "project"
    for rel_path, content in files.items():
        fpath = project_dir / rel_path
        fpath.parent.mkdir(parents=True, exist_ok=True)
        fpath.write_text(content, encoding="utf-8")

    old_path = sys.path[:]
    sys.path.insert(0, str(project_dir))
    # Track modules to clean up
    added_modules: list[str] = []
    try:
        tags_mod = importlib.import_module("tags")
        added_modules.append("tags")

        # Import subroutine modules
        for rel_path in sorted(files):
            if (
                rel_path.startswith("subroutines/")
                and rel_path.endswith(".py")
                and rel_path != "subroutines/__init__.py"
            ):
                mod_name = rel_path[:-3].replace("/", ".")
                importlib.import_module(mod_name)
                added_modules.append(mod_name)

        # subroutines package itself
        if "subroutines/__init__.py" in files:
            if "subroutines" not in sys.modules:
                importlib.import_module("subroutines")
            added_modules.append("subroutines")

        main_mod = importlib.import_module("main")
        added_modules.append("main")

        return {
            "logic": main_mod.logic,
            "mapping": tags_mod.mapping,
        }
    finally:
        sys.path[:] = old_path
        for key in added_modules:
            sys.modules.pop(key, None)


