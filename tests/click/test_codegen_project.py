class TestProjectWithSubroutines:
    """Project generation with subroutines."""

    def _make_program_with_sub(self):
        Button = Bool("Button")
        Light = Bool("Light")
        SubLight = Bool("SubLight")

        with Program() as logic:
            with Rung(Button):
                out(Light)
                call("init")

            with subroutine("init"):
                with Rung():
                    out(SubLight)

        mapping = TagMap(
            {Button: x[1], Light: y[1], SubLight: y[2]},
            include_system=False,
        )
        return logic, mapping

    def test_file_structure(self, tmp_path: Path):
        """Project with subroutines has correct file layout."""
        logic, mapping = self._make_program_with_sub()
        files = _project_from_program(logic, mapping, tmp_path)

        assert "main.py" in files
        assert "tags.py" in files
        assert "subroutines/__init__.py" in files
        assert "subroutines/init.py" in files
        assert files["subroutines/__init__.py"] == ""

    def test_subroutine_has_decorator(self, tmp_path: Path):
        """Subroutine file uses @subroutine decorator pattern."""
        logic, mapping = self._make_program_with_sub()
        files = _project_from_program(logic, mapping, tmp_path)

        sub_py = files["subroutines/init.py"]
        assert '@subroutine("init")' in sub_py
        assert "def init():" in sub_py
        assert "from tags import" in sub_py

    def test_main_calls_func_not_string(self, tmp_path: Path):
        """main.py uses call(func) not call('name')."""
        logic, mapping = self._make_program_with_sub()
        files = _project_from_program(logic, mapping, tmp_path)

        main_py = files["main.py"]
        assert "call(init)" in main_py
        assert 'call("init")' not in main_py
        assert "from subroutines.init import init" in main_py

    def test_exec_round_trip_with_subroutine(self, tmp_path: Path):
        """Full exec round-trip with subroutine."""
        logic, mapping = self._make_program_with_sub()

        # Get original CSV
        original_bundle = pyrung_to_ladder(logic, mapping)
        original_rows = list(original_bundle.main_rows)

        files = _project_from_program(logic, mapping, tmp_path)
        ns = _exec_project(files, tmp_path)

        # Re-export
        reproduced_bundle = pyrung_to_ladder(ns["logic"], ns["mapping"])
        reproduced_rows = list(reproduced_bundle.main_rows)

        assert original_rows == reproduced_rows

    def test_subroutine_with_conditions(self, tmp_path: Path):
        """Subroutine with conditional rungs round-trips."""
        Enable = Bool("Enable")
        Cond = Bool("Cond")
        Y1 = Bool("Y1")
        Y2 = Bool("Y2")

        with Program() as logic:
            with Rung(Enable):
                call("worker")

            with subroutine("worker"):
                with Rung(Cond):
                    out(Y1)
                with Rung():
                    out(Y2)

        mapping = TagMap(
            {Enable: x[1], Cond: x[2], Y1: y[1], Y2: y[2]},
            include_system=False,
        )

        original_bundle = pyrung_to_ladder(logic, mapping)
        original_rows = list(original_bundle.main_rows)

        files = _project_from_program(logic, mapping, tmp_path)
        ns = _exec_project(files, tmp_path)

        reproduced_bundle = pyrung_to_ladder(ns["logic"], ns["mapping"])
        reproduced_rows = list(reproduced_bundle.main_rows)

        assert original_rows == reproduced_rows


class TestPerFileImports:
    """Each file should only import what it uses."""

    def test_subroutine_imports_only_its_tags(self, tmp_path: Path):
        """Sub1 uses X001/Y001, Sub2 uses X002/Y002 — imports are isolated."""
        X1 = Bool("X1")
        X2 = Bool("X2")
        Y1 = Bool("Y1")
        Y2 = Bool("Y2")
        Main = Bool("Main")

        with Program() as logic:
            with Rung(Main):
                call("sub1")
                call("sub2")

            with subroutine("sub1"):
                with Rung(X1):
                    out(Y1)

            with subroutine("sub2"):
                with Rung(X2):
                    out(Y2)

        mapping = TagMap(
            {Main: c[1], X1: x[1], X2: x[2], Y1: y[1], Y2: y[2]},
            include_system=False,
        )
        files = _project_from_program(logic, mapping, tmp_path)

        sub1 = files["subroutines/sub1.py"]
        sub2 = files["subroutines/sub2.py"]

        # sub1 imports X1, Y1 but not X2, Y2
        assert "X001" in sub1 or "X1" in sub1
        assert "Y001" in sub1 or "Y1" in sub1
        # sub2 imports X2, Y2 but not X1, Y1
        assert "X002" in sub2 or "X2" in sub2
        assert "Y002" in sub2 or "Y2" in sub2

    def test_main_imports_mapping(self, tmp_path: Path):
        """main.py always imports mapping from tags."""
        Button = Bool("Button")
        Light = Bool("Light")

        with Program() as logic:
            with Rung(Button):
                out(Light)

        mapping = TagMap({Button: x[1], Light: y[1]}, include_system=False)
        files = _project_from_program(logic, mapping, tmp_path)

        main_py = files["main.py"]
        assert "mapping" in main_py
        # mapping should come from tags
        from_tags_line = [ln for ln in main_py.splitlines() if ln.startswith("from tags import")]
        assert len(from_tags_line) == 1
        assert "mapping" in from_tags_line[0]

    def test_main_uses_or_for_two_way_bool_or(self, tmp_path: Path):
        """Two-way BOOL OR renders with Or() and imports Or."""
        A = Bool("A")
        B = Bool("B")
        Y = Bool("Y")

        with Program() as logic:
            with Rung(Or(A, B)):
                out(Y)

        mapping = TagMap({A: x[1], B: x[2], Y: y[1]}, include_system=False)
        files = _project_from_program(logic, mapping, tmp_path)

        main_py = files["main.py"]
        assert "with Rung(" in main_py
        assert "Or(" in main_py

    def test_subroutine_imports_click_block_for_raw_range(self, tmp_path: Path):
        """Subroutine using reset(c.select(...)) must import c from pyrung.click."""
        Enable = Bool("Enable")
        Coil = Bool("Coil")
        Bits = Block("Bits", TagType.BOOL, 1, 3)

        with Program() as logic:
            with Rung(Enable):
                call("worker")

            with subroutine("worker"):
                with Rung():
                    out(Coil)
                    reset(Bits.select(1, 3))

        mapping = TagMap(
            {Enable: x[1], Coil: y[1], Bits: c.select(1004, 1006)},
            include_system=False,
        )
        files = _project_from_program(logic, mapping, tmp_path)
        sub_py = files["subroutines/worker.py"]

        # Must have a pyrung.click import that includes 'c'
        click_import = [
            ln for ln in sub_py.splitlines() if ln.startswith("from pyrung.click import")
        ]
        assert click_import, "subroutine should have a pyrung.click import line"
        assert "c" in click_import[0].split("import")[1]

        # Verify the project can actually be imported and executed
        ns = _exec_project(files, tmp_path)
        assert ns["logic"] is not None

    def test_subroutine_imports_click_block_for_pointer(self, tmp_path: Path):
        """Subroutine using copy(DH[idx], dest) must import dh from pyrung.click."""
        from pyrung.click.codegen.collector import _collect_operands
        from pyrung.click.codegen.models import (
            _AnalyzedRung,
            _InstructionInfo,
            _SubroutineInfo,
        )
        from pyrung.click.codegen.project_emitter import _generate_project

        main_rung = _AnalyzedRung(
            comment=None,
            condition_tree=None,
            instructions=[_InstructionInfo(af_token='call("worker")', branch_tree=None, pins=[])],
        )
        sub_rung = _AnalyzedRung(
            comment=None,
            condition_tree=None,
            instructions=[
                _InstructionInfo(af_token="copy(DH[DS134],DH051)", branch_tree=None, pins=[]),
            ],
        )

        subroutines = [_SubroutineInfo(name="worker", analyzed=[sub_rung])]
        all_rungs = [main_rung, sub_rung]
        collection = _collect_operands(all_rungs, None)
        collection.has_subroutine = True

        files = _generate_project([main_rung], collection, None, subroutines)
        sub_py = files["subroutines/worker.py"]

        # Must have dh imported from pyrung.click
        click_import = [
            ln for ln in sub_py.splitlines() if ln.startswith("from pyrung.click import")
        ]
        assert click_import, "subroutine should have a pyrung.click import line"
        assert " dh" in click_import[0] or ",dh" in click_import[0]

        # Should use lowercase block variable, not uppercase Click prefix
        assert "dh[" in sub_py
        assert "DH[" not in sub_py


class TestSubroutineCallsSubroutine:
    """Subroutine that calls another subroutine.

    Note: Click hardware doesn't support nested subroutine calls, so
    we test the codegen import structure directly (not via CSV round-trip).
    """

    def test_cross_sub_import(self, tmp_path: Path):
        """sub_a calls sub_b — sub_a.py imports from subroutines.sub_b."""
        from pyrung.click.codegen.collector import _collect_operands
        from pyrung.click.codegen.models import (
            _AnalyzedRung,
            _InstructionInfo,
            _SubroutineInfo,
        )
        from pyrung.click.codegen.project_emitter import _generate_project

        # Build analyzed rungs by hand (bypassing CSV which rejects nested calls)
        main_rung = _AnalyzedRung(
            comment=None,
            condition_tree=None,
            instructions=[_InstructionInfo(af_token='call("sub_a")', branch_tree=None, pins=[])],
        )
        sub_a_rung = _AnalyzedRung(
            comment=None,
            condition_tree=None,
            instructions=[
                _InstructionInfo(af_token='call("sub_b")', branch_tree=None, pins=[]),
                _InstructionInfo(af_token="out(Y001)", branch_tree=None, pins=[]),
            ],
        )
        sub_b_rung = _AnalyzedRung(
            comment=None,
            condition_tree=None,
            instructions=[_InstructionInfo(af_token="out(Y002)", branch_tree=None, pins=[])],
        )

        subroutines = [
            _SubroutineInfo(name="sub_a", analyzed=[sub_a_rung]),
            _SubroutineInfo(name="sub_b", analyzed=[sub_b_rung]),
        ]

        all_rungs = [main_rung, sub_a_rung, sub_b_rung]
        collection = _collect_operands(all_rungs, None)
        collection.has_subroutine = True

        files = _generate_project([main_rung], collection, None, subroutines)

        sub_a = files["subroutines/sub_a.py"]
        assert "from subroutines.sub_b import sub_b" in sub_a
        assert "call(sub_b)" in sub_a


